    end
  end

  on_linux do
    if Hardware::CPU.arm?
      url "https://github.com/maneeshchaturvedi/homebrew-specflow/releases/download/v1.1.0-alpha/specflow-linux-arm64"
      sha256 "a335cfecccbde259f4fdc9487a225e2af03d128601355cac53f3d9410ee6fa79"
