      url "https://github.com/maneeshchaturvedi/homebrew-specflow/releases/download/v1.1.0-alpha/specflow-darwin-arm64"
      sha256 "a51b65d6a0d1d2261eca142fb9a0e166cea031e2a9e7f7e7387124abdfbbdf68"
